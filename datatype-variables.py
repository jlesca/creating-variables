# TIPO DE DATOS EN LAS VARIABLES
# Los datos que guardemos en nuestras variables pueden ser:
# Texto, númericos, decimales, verdadero o falso, entre otros.
# Python los identifica de manera abreviada por sus definiciones en inglés.

# Podemos especificar que tipo de dato queremos que sea el valor de nuestra variable.

string = str(5) # La variable string valdrá = "5"
number = int(5) # La variable number valdrá = 5
decimal = float(5) # La variable decimal valdrá = "5.0"
print(string, number, decimal) # Le pido que muestre las variables.
