# MULTIPLES VALORES EN MULTIPLES VARIABLES
# Python nos permite asignar múltiples valores y múltipleas variables en una sola línea.

a, b, c = 'red', 'green', 'blue' # Creo las variables y le asigno los valores a cada una, separando con una coma.
print(a) # Muestro la variable
print(b) # Muestro la variable
print(c) # Muestro la variable

# También podríamos mostrar los valores del siguiente modo:
print(a, b, c) # Muestro las variables en una sola línea.
