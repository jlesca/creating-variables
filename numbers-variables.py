# VARIABLES NUMERICAS
# En Python podemos almacenar 3 tipos de valores numéricos. 
# 1. Integer (int)
# 2. Float (float)
# 3. Complex (complex)

a = 1 # int
b = 1.5 # float
c = 1j # complex

print(type(a)) # Mostrará que la clase es int
print(type(b)) # Mostrará que la clase es float
print(type(c)) # Mostrará que la clase es complex
